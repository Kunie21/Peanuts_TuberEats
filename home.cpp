#include "main.h"
#include "renderer.h"
#include "fade.h"
#include "home.h"
#include "texture2d.h"
#include "character_select.h"
#include "rocket_select.h"
#include "wallet.h"

//*****************************************************************************
// マクロ定義
//*****************************************************************************

//*****************************************************************************
// グローバル変数
//*****************************************************************************
static BOOL				g_Load = FALSE;


//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitHome(void)
{
	InitRocketSelect();
	InitCharacterSelect();
	InitWallet();

	g_Load = TRUE;
	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitHome(void)
{
	if (g_Load == FALSE) return;

	UninitRocketSelect();
	UninitCharacterSelect();
	UninitWallet();

	g_Load = FALSE;
}

//=============================================================================
// 更新処理
//=============================================================================
void UpdateHome(void)
{
	UpdateRocketSelect();
	UpdateCharacterSelect();
	UpdateWallet();
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawHome(void)
{
	DrawRocketSelect();
	DrawCharacterSelect();
	DrawWallet();
}
