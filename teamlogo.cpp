//*****************************************************************************
// 
//*****************************************************************************
#define TEXTURE_WIDTH				(SCREEN_WIDTH)	// 
#define TEXTURE_HEIGHT				(SCREEN_HEIGHT)	// 

//#define TEXTURE_WIDTH_LOGO			(1154)			// 
//#define TEXTURE_HEIGHT_LOGO			(693)			// 

#define TEAMLOGO_TIME				(100)			// 

//*****************************************************************************
// 
//*****************************************************************************
static int				g_Time = 0;
static BOOL				g_Load = FALSE;

static TEXTURE2D_DESC*	g_td;

//=============================================================================
// 
//=============================================================================
HRESULT InitTeamLogo(void)
{
	g_td = new TEXTURE2D_DESC;

	g_td->tex = TEXTURE_LABEL_TEAMLOGO;

	g_Time = 0;

	g_Load = TRUE;
	return S_OK;
}

//=============================================================================
// 
//=============================================================================
void UninitTeamLogo(void)
{
	if (g_Load == FALSE) return;

	delete g_td;

	g_Load = FALSE;
}

//=============================================================================
// 
//=============================================================================
void UpdateTeamLogo(void)
{
	if (g_Time++ > TEAMLOGO_TIME) { g_Time = 0; SetFade(FADE_OUT, MODE_LOADING); }
}

//=============================================================================
// 
//=============================================================================
void DrawTeamLogo(void)
{
	DrawTexture2D(g_td);
}
